package com.kodilla.com.kodilla.basic_assertion;

public class Calculator {

    public int sum(int a, int b) {
        return a + b;
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int exponentiation(int a, int n) {
        return (int) Math.pow(a, n);
    }
}

