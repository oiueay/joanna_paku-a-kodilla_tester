package com.kodilla.basic_assertion;

public class Calculator {

    public int sum(double a, double b) {
        return (int) (a + b);
    }

    public int subtract(int a, int b) {
        return a - b;
    }

    public int exponentiation(double a, double n) {
        return (int) Math.pow(a, n);
    }
}

